    private void btn_generateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generateActionPerformed
        int topicNumber=Integer.parseInt((String) combo_temas.getSelectedItem());
        pnl_main.removeAll();
        generateBtns(topicNumber);
        
        int beadNumber = Integer.parseInt((String)combo_bolas.getSelectedItem());
        int componentNumber = pnl_main.getComponentCount();
        
        for (int i = 0; i < beadNumber; i++) {
            int randomBtn = (int) (Math.random()*componentNumber+1);
            while(randomBtn > topicNumber -1){
                randomBtn = (int)(Math.random()*componentNumber+1);
            }
            JButton btnSelected=(JButton) pnl_main.getComponent(randomBtn);
            
            if(btnSelected.isEnabled()){
                boolean isActive = true;
                while(isActive){
                    randomBtn = (int)(Math.random()*componentNumber+1);
                    btnSelected = (JButton) pnl_main.getComponent(randomBtn);
                    if(!btnSelected.isEnabled()){
                        isActive = false;
                    }
                }
            }           
            btnSelected.setEnabled(true);     
        }
    }//GEN-LAST:event_btn_generateActionPerformed

