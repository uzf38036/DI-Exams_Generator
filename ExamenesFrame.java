    private void generateBtns(int numTopics) {
        for (int i = 0; i < numTopics; i++) {
            JButton btn_topics=new JButton(String.valueOf(i+1));
            btn_topics.setEnabled(false);
            String topic= topicList.get(i);
            btn_topics.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e){
                    JOptionPane.showMessageDialog(pnl_main, topic, "Titulo del tema", JOptionPane.INFORMATION_MESSAGE
                    );
                }
            });
            pnl_main.add(btn_topics);
                    
        }
    }
    
    private void addTopics(File topics){
        
        try {
            String line;
            BufferedReader br=new BufferedReader(new FileReader(topics));
            while((line=br.readLine()) != null){
                topicList.add(line);
            }
            br.close();
            
        } catch (FileNotFoundException e) {
            System.out.println("File not found");
        } catch(IOException ex) {
            System.out.println("Error while reading file");
        }
    }
    
